package cn.edu.zucc.take_out.model;

public class BeanCollection {
     private int userId;
     private int shopId;
     private int requsetNumber;
     private int yetFinished;
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getShopId() {
		return shopId;
	}
	public void setShopId(int shopId) {
		this.shopId = shopId;
	}
	public int getRequsetNumber() {
		return requsetNumber;
	}
	public void setRequsetNumber(int requsetNumber) {
		this.requsetNumber = requsetNumber;
	}
	public int getYetFinished() {
		return yetFinished;
	}
	public void setYetFinished(int yetFinished) {
		this.yetFinished = yetFinished;
	}
     
}
